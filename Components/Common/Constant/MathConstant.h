#ifndef MATH_CONSTANT_H
#define MATH_CONSTANT_H

// float型の円周率
const float kPiFloat = (3.1415926535897932384626433832795f);
// double型の円周率
const double kPi = (3.1415926535897932384626433832795);

// float型の半分の円周率
const float kHalfPiFloat = (kPiFloat * 0.5f);
// double型の半分の円周率
const double kHalfPi = (kPi * 0.5);

// float型の２倍の円周率
const float kTwoPiFloat = (kPiFloat * 2.0f);
// double型の半分の円周率
const double kTwoPi = (kPi * 2.0);

#endif